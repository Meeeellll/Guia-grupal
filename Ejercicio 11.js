/* Realizar programa donde se Introducen de 3 números y el no programa indica cuál es el
mayor de los 3. */

function numeromayor(){
    var numero1 = 9
    var numero2 = 14
    var numero3 = 25

    let mayor = Math.max(numero1, numero2, numero3)
    console.log('El numero mayor es: '+mayor)
}

numeromayor()